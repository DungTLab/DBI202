-- Select all:
SELECT * 
FROM BOOK;

-- Select books with quantity greater than 5:
SELECT * 
FROM BOOK
WHERE quantity > 5;

-- Select books with quantiy less than or equal to 5:
SELECT * 
FROM BOOK
WHERE quantity <= 5;

-- Select books with price greater than 20:
SELECT *
FROM BOOK
WHERE price > 20;

-- Select books with price less than or equal to 20:
SELECT *
FROM BOOK
WHERE price <= 20;